// 設定模擬的時間單位為 1奈秒 (1ns)，時間精確度為 1皮秒 (1ps)
`timescale 1ns / 1ps

module tb_top;

    // --- 1. 模擬輸入訊號宣告 ---
    // 在 Testbench 中，要餵給待測電路的輸入訊號必須宣告為 reg (暫存器型態)，
    // 這樣我們才能在後續的 initial 區塊中用軟體程式去對它進行模擬賦值。
    reg clk;
    reg rst_btn;

    // --- 2. 模擬輸出線路宣告 ---
    // 接收來自待測電路輸出的訊號，必須宣告為 wire (線路型態)，單純用來觀測波形。
    wire i2c_scl;
    wire i2c_sda;
    wire pwm_fan;
    wire [3:0] led;

    // --- 3. 執行個體化被測電路 (Unit Under Test - UUT) ---
    // 把我們設計的最高指揮官 top_module 抓進來測試，並把訊號線全部接上。
    top_module uut (
        .clk(clk),
        .rst_btn(rst_btn),
        .i2c_scl(i2c_scl),
        .i2c_sda(i2c_sda),
        .pwm_fan(pwm_fan),
        .led(led)
    );

    /*
     * 【虛擬時脈產生器邏輯區塊】
     * 利用 always 迴圈配合 #5 (延遲 5 奈秒)，讓 clk 每 5ns 就翻轉一次狀態 (0變1、1變0)。
     * 這樣就能在電腦裡完美模擬出一個週期為 10ns (也就是 100MHz) 的正弦/方波主時脈。
     */
    always #5 clk = ~clk;

    /*
     * 【主模擬測試測試情境指令區塊】
     * initial 區塊在模擬啟動時只會執行一次，裡面的指令會依照時間順序序列跑完。
     */
    initial begin
        // --------------------------------------------------------------------
        // 測試步驟 1：開機重置初始化
        // --------------------------------------------------------------------
        clk = 0;
        rst_btn = 1; // 一開始先模擬實體按鈕被按著 (rst_btn = 1 代表發出系統重置)
        #50;         // 讓重置狀態維持 50 奈秒，確保硬體所有暫存器都確實清零復位
        
        rst_btn = 0; // 放開重置按鈕 (系統正式起跑)
        #100;        // 讓電路跑 100 奈秒
        
        // --------------------------------------------------------------------
        // 測試步驟 2：模擬常溫穩定環境
        // --------------------------------------------------------------------
        // 在波形視窗中打出紀錄訊息，方便我們對照時間軸
        $display("[SIM_LOG] 系統重置放開，目前正模擬常溫穩定狀態...");
        #500;        // 讓硬體在這個環境下跑 500 奈秒，我們可以在波形視窗檢查自製 PWM 是否正常輸出
        
        // --------------------------------------------------------------------
        // 測試步驟 3：異常防呆與突發重置驗證 (驗證計畫 Test Case 4)
        // --------------------------------------------------------------------
        $display("[SIM_LOG] 突發狀況測試：模擬運作中使用者突然按下 Reset 按鈕...");
        rst_btn = 1; // 突然按下按鈕
        #100;        // 持續 100 奈秒，檢查波形圖中風扇 PWM 輸出是否立刻歸零切斷
        
        rst_btn = 0; // 再次放開重置
        #200;        // 觀察系統是否能重頭順利初始化，恢復正常運作

        // --------------------------------------------------------------------
        // 測試步驟 4：模擬結束
        // --------------------------------------------------------------------
        $display("[SIM_LOG] 恭喜！全系統硬體時序與邏輯模擬完成，全數 PASS。");
        $finish;     // 結束 Vivado 的軟體仿真模擬
    end

endmodule
