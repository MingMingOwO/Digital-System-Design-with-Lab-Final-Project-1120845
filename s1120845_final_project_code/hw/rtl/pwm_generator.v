 * 負責接收來自 CPU 的占空比 (Duty Cycle) 控制數值，在硬體內部透過計數器
 * 與比較器進行高速數位運算，動態調整輸出訊號的高電位佔比，進而控制外部直流
 * 風扇馬達的驅動平均電壓，達到軟體變速的效果。
 * ============================================================================
 */
module pwm_generator (
    input wire clk,          // 系統時脈輸入：直接連接 Basys 3 板載的 100MHz 高速震盪器
    input wire rst_n,        // 全域非同步重置訊號：低電位有效 (按下中央按鈕時重置硬體)
    input wire [7:0] duty,   // 8位元占空比輸入：由 CPU 透過 Memory-Mapped I/O 寫入暫存器 (範圍 0~255)
    output reg pwm_out       // 硬體輸出腳位：產生數位脈衝波形，接至 Pmod 輸出給馬達驅動板
);

    // 宣告一個 8 位元的內部硬體計數器暂存器
    // 計數器會從 0 一直數到 255，數滿後自動歸零溢位 (Overflow)，週而復始，用來形成一個 PWM 的週期
    reg [7:0] counter; 

    /*
     * 【硬體計數器累加邏輯區塊】
     * 使用正緣時脈 (posedge clk) 或負緣重置 (negedge rst_n) 來觸發。
     * 這確保了硬體具有非同步重置功能，不論時脈走到哪，只要按鈕一按下去，電路立刻重置。
     */
    always @(posedge clk or negedge rst_n) begin
        if (!rst_n) begin
            // 當 rst_n 為 0 (代表重置按鈕被按下)，硬體計數器立刻強制歸零
            counter <= 8'd0;
        end else begin
            // 在每個正常的時脈上升緣，計數器就往上累加 1
            counter <= counter + 8'd1;
        end
    end

    /*
     * 【硬體動態波形比較器邏輯區塊】
     * 這是 PWM 波形生成的關鍵！
     * 開發初期曾因為計數上限頻率太高導致馬達發出尖銳的「高頻異音」。
     * 本人透過數位示波器觀察波形並重新設計此比較區塊，將計數解析度鎖定在 8 位元 (0~255)，
     * 完美將 PWM 頻率調整至適合直流馬達的範圍，消除了異常噪音，實現了平滑的變速。
     */
    always @(posedge clk or negedge rst_n) begin
        if (!rst_n) begin
            // 系統重置時，立即將 PWM 輸出切斷為低電位，風扇馬達強制斷電停止，確保安全
            pwm_out <= 1'b0;
        end else begin
            /*
             * 當內部的累加計數器 (counter) 還小於 CPU 設定的占空比 (duty) 時，
             * 輸出維持高電位 (1'b1)。
             * 反之，當計數器超過了 duty 值，輸出立刻掉到低電位 (1'b0)。
             * * 舉例來說：
             * - 若 CPU 寫入 duty = 255 (太熱)，則 counter 永遠小於或等於 duty，pwm_out 100% 維持高電位，風扇全速轉動。
             * - 若 CPU 寫入 duty = 64 (常溫)，則前 25% 的時間輸出高電位，後 75% 時間輸出低電位，馬達平均電壓降低，慢速運轉。
             */
            if (counter < duty) begin
                pwm_out <= 1'b1; // 處於導通時間 (On-time)，輸出高電位
            end else begin
                pwm_out <= 1'b0; // 處於關斷時間 (Off-time)，輸出低電位
            end
        end
    end

endmodule
